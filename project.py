def add(x, y):
    """Returns the sum of x and y"""
    return x + y

def subtract(x, y):
    """Returns the difference of x and y"""
    return x - y

def multiply(x, y):
    """Returns the product of x and y"""
    return x * y

def divide(x, y):
    """Returns the quotient of x and y"""
    return x / y

def power(x, y):
    """Returns x to the power of y"""
    return x ** y

def square_root(x):
    """Returns the square root of x"""
    return x ** 0.5